# two types of typa casting
# explicit: done by developer
a = "2"
b = "45"

print(int(a)+int(b))


# implicit: While performing any operations on variables with different data types in Python, one of the variable's data types will be changed to the higher data type. According to the level, one data type is converted into other by the Python interpreter itself (automatically). This is called, implicit type casting in python.

c = 1.9
d = 8

print(c+d)
