a=23
b=6

print("the value of", a, "+", b, "is:", a+b)
print("the value of", a, "-", b, "is:", a-b)
print("the value of", a, "*", b, "is:", a*b)
print("the value of", a, "/", b, "is:", a/b)
print("the value of", a, "%", b, "is:", a%b)  #modulus
print("the value of", a, "//", b, "is:", a//b)  #floor divide
print("the value of", a, "**", b, "is:", a**b)  #exponentional